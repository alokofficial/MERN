// valid only inside the webpage document

// 1. getElementById
// 2. getElementsByClassName
// 3. getElementsByTagName
// 4. querySelector
// 5. querySelectorAll
// examples of DOM

// 1. getElementById
document.getElementById("demo").innerHTML = "Hello World";

// 2. getElementsByClassName
document.getElementsByClassName("demo")[0].innerHTML = "Hello World";

// 3. getElementsByTagName
document.getElementsByTagName("p")[0].innerHTML = "Hello World";

// 4. querySelector
document.querySelector("p").innerHTML = "Hello World";

// 5. querySelectorAll
document.querySelectorAll("p")[0].innerHTML = "Hello World";

